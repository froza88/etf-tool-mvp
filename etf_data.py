# 筛选ETF
def filter_etfs(filters):
    result = ETFs
    
    if filters.get("type"):
        result = [etf for etf in result if filters["type"] in etf["type"]]
    
    if filters.get("scale_min"):
        result = [etf for etf in result if etf["scale"] >= float(filters["scale_min"])]
    
    if filters.get("scale_max"):
        result = [etf for etf in result if etf["scale"] <= float(filters["scale_max"])]
    
    if filters.get("fee_max"):
        result = [etf for etf in result if etf["fee"] <= float(filters["fee_max"])]
    
    if filters.get("return_min"):
        result = [etf for etf in result if etf["year_1_return"] >= float(filters["return_min"])]
    
    if filters.get("category"):
        result = [etf for etf in result if etf["category"] == filters["category"]]
    
    if filters.get("keyword"):
        keyword = filters["keyword"].lower()
        result = [etf for etf in result if keyword in etf["name"].lower() or keyword in etf["code"].lower()]
    
    return result
