def generate_etf_data():
    """生成ETF模拟数据"""
    etfs = []
    
    for code, name, etf_type, category in ETF_LIST:
        # 模拟数据（合理范围）
        scale = round(random.uniform(5, 500), 1)  # 规模：5-500亿
        fee = round(random.uniform(0.3, 0.8), 2)  # 费率：0.3%-0.8%
        tracking_error = round(random.uniform(0.01, 0.2), 2)  # 跟踪误差：0.01%-0.2%
        
        # 收益率（根据类型调整）
        if "债券" in etf_type or "货币" in etf_type:
            year_1_return = round(random.uniform(1, 5), 1)
            year_3_return = round(random.uniform(5, 15), 1)
            max_drawdown = round(random.uniform(-2, -0.5), 1)
            sharpe_ratio = round(random.uniform(1.0, 2.0), 2)
        elif "商品" in etf_type:
            year_1_return = round(random.uniform(-10, 20), 1)
            year_3_return = round(random.uniform(-5, 30), 1)
            max_drawdown = round(random.uniform(-20, -5), 1)
            sharpe_ratio = round(random.uniform(0.3, 0.8), 2)
        else:  # 股票型
            year_1_return = round(random.uniform(-20, 30), 1)
            year_3_return = round(random.uniform(-10, 50), 1)
            max_drawdown = round(random.uniform(-60, -10), 1)
            sharpe_ratio = round(random.uniform(0.3, 1.0), 2)
        
        # 发行日期（2010-2023年随机）
        start_date = datetime(2010, 1, 1)
        end_date = datetime(2023, 12, 31)
        time_between = end_date - start_date
        days_between = time_between.days
        random_days = random.randint(0, days_between)
        launch_date = start_date + timedelta(days=random_days)
        
        # 发行商（随机）
        issuers = ["华夏基金", "华泰柏瑞", "南方基金", "易方达", "嘉实基金", "广发基金", "汇添富", "国泰基金", "华宝基金", "富国基金"]
        issuer = random.choice(issuers)
        
        # 日均成交额（根据规模调整）
        volume = round(scale * random.uniform(0.01, 0.1), 1)
        
        # 跟踪指数（简化）
        underlying = f"{name}指数"
        
        # 前十大持仓（简化）
        top_holdings = ["持仓1 10%", "持仓2 8%", "持仓3 6%", "持仓4 5%", "持仓5 4%"]
        
        etf = {
            "code": code,
            "name": name,
            "type": etf_type,
            "scale": scale,
            "fee": fee,
            "tracking_error": tracking_error,
            "year_1_return": year_1_return,
            "year_3_return": year_3_return,
            "max_drawdown": max_drawdown,
            "sharpe_ratio": sharpe_ratio,
            "launch_date": launch_date.strftime("%Y-%m-%d"),
            "issuer": issuer,
            "underlying": underlying,
            "top_holdings": top_holdings,
            "volume": volume,
            "category": category
        }
        etfs.append(etf)
    
    return etfs

if __name__ == "__main__":
    etfs = generate_etf_data()
    
    # 输出为Python代码格式
    print("# ETF模拟数据库")
